package com.ruinscraft.soundemotes;

public class SoundEmote {

    private String name;
    private String url;

    public SoundEmote(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

}
